--What is SQL?
--Structured Query Language (SQL) is a standard programming language specifically designed for managing and manipulating relational databases. 
--It allows users to create, read, update, and delete data within a database.
--pronounced as "sequel" or by its initials "S-Q-L".

--Why learn SQL?
--Talk to Data
--SQL is essential for anyone working with data, as it provides a powerful and efficient way to interact with databases.
--It's widely used in various industries for data analysis, reporting, and application development.
--Learning SQL can enhance your career prospects, as many job roles require SQL knowledge.

--What is Database?
--A database (container) is an organized collection of data that can be easily accessed, managed, and updated.
--purpose of a database is to store data in a structured way, allowing for efficient retrieval and manipulation of that data.
--Excel, txt and other file formats are to small scale data storage solutions, while databases are designed to handle large volumes of data and complex queries.
--it's a professional and secure way to manage data.

--DBMS
--A Database Management System (DBMS) is software that enables users to create, manage, and interact with databases.
--People, Applications and Power BI connect to the DBMS
--Servers host the DBMS (Cloud or On-Premises))

--Vocabulary:
--Database :: Stores Data
--SQL :: Speak to Database
--DBMS :: Manage Database
--Server :: Where Databse Lives

--Database Types:
--Relational Databases (RDBMS): Store data in tables with rows and columns. Examples: MySQL, PostgreSQL, Oracle, Microsoft SQL Server.
--SQL is primarily used with relational databases.
--No SQL Databases: Designed for unstructured data and flexible schemas. Examples: MongoDB, Cassandra, Redis.
--Types of No SQL Databases:
--Key-Value Stores: Store data as key-value pairs. Examples: Redis, Amazon DynamoDB.
--Column-Based Databases: Store data in columns rather than rows. Examples: Apache Cassandra, HBase, Amazon Redshift.
--Graph Databases: Store data in graph structures with nodes and edges. Examples: Neo4j, Amazon Neptune.
--Document-Based Databases: Store data as documents, often in JSON or XML format. Examples: MongoDB, CouchDB.

--Databse Structure:
--Server can host multiple Databases
--Databeses can have multiple Schemas (Tables)
--A table is made up of rows and columns with each column representing a specific attribute of the data and each row representing a single record.
--Primary Key: A unique identifier for each record in a table.
--Value: The actual data stored in the table's cells.
--Data Types: Define the type of data that can be stored in each column (e.g., INTEGER, VARCHAR, DATE).
--Numerie: Integer (1, 2, 30), Float, Decimal (3.14, 100,50)
--Text as Strings: Char, Varchar, Text ('Maria', 'Hello World')
--Date and Time: Date ('2025-10-30'), Time ('09:30:00'), Timestamp

--SQL Commands
--DDL (Data Definition Language): Used to define and manage database structures.
--Create, Alter, Drop
--DML (Data Manipulation Language): Used to retrieve and manipulate data within the database.
--Insert, Update, Delete
--DQL (Data Query Language): A subset of DML focused on querying data.
--Select

